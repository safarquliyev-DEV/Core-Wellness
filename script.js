function openModal(item) {
  document.getElementById('modalTitle').innerText = item.title;

  let html = '';

  // PRICE + TIME
  if (item.durations && item.durations.length > 0) {
    html += `<div class="price-box">`;
    item.durations.forEach(d => {
      html += `
        <div class="price-row">
          <span>${d.time}</span>
          <strong>${d.price}</strong>
        </div>
      `;
    });
    html += `</div><hr>`;
  }

  // DESCRIPTION
  html += `<p>${item.full}</p>`;

  document.getElementById('modalText').innerHTML = html;
  document.getElementById('modal').style.display = 'flex';
}


function closeModal() {
  document.getElementById('modal').style.display = 'none';
}

function setGender(gender, btn) {
  currentGender = gender;
  currentCategory = '';

  document.querySelectorAll('.gender-switch button')
    .forEach(b => b.classList.remove('active'));

  btn.classList.add('active');

  buildTabs();
  updateBackground();

  // 👇 SPA ETIQUETTE üçün scroll
  if (gender === 'spaEtiquette') {
    setTimeout(() => {
      const el = document.getElementById('etiquetteSection');
      if (el) {
        el.scrollIntoView({ behavior: 'smooth', block: 'start' });
      }
    }, 200);
  }
}

