let currentGender = 'ladies';
let currentCategory = '';


function toSlug(text) {
  return text.toLowerCase().replace(/\s+/g, '-');
}

function updateBackground() {
  const body = document.body;
  body.className = "page-menu";

  if (!currentCategory) {
    // sadəcə gender background
    body.classList.add(currentGender);
  } else {
    const category = toSlug(currentCategory);
    body.classList.add(`${currentGender}-${category}`);
  }
}


function buildTabs() {
  const tabs = document.getElementById('categoryTabs');
  const cards = document.getElementById('cards');

  tabs.innerHTML = '';
  cards.innerHTML = '';

  if (currentGender === 'spaEtiquette') {
    cards.innerHTML = `
  <div class="etiquette-wrapper" id="etiquetteSection">
    ${data.spaEtiquette}
  </div>
`;
    return;
  }

  const categories = Object.keys(data[currentGender]);
  currentCategory = '';

  categories.forEach(cat => {
    const b = document.createElement('button');
    b.textContent = cat;

    b.onclick = () => {
      document.querySelectorAll('.tabs button')
        .forEach(t => t.classList.remove('active'));

      b.classList.add('active');
      currentCategory = cat;

      buildCards();
      updateBackground();
    };

    tabs.appendChild(b);
  });
}


function buildCards() {
  const wrap = document.getElementById('cards');
  wrap.innerHTML = '';

  data[currentGender][currentCategory].forEach((item, i) => {
    const card = document.createElement('div');
    card.className = 'card';

    let priceHTML = '';
    if (item.durations && item.durations.length > 0) {
      priceHTML += `<div class="price-box">`;
      item.durations.forEach(d => {
        priceHTML += `
          <div class="price-row">
            <span>${d.time}</span>
            <strong>${d.price}</strong>
          </div>
        `;
      });
      priceHTML += `</div>`;
    }

    card.innerHTML = `
  <div class="card-header header-row">
    <strong class="card-title">${item.title}</strong>

    ${item.durations && item.durations.length
        ? `<span class="header-price">
            ${item.durations.map(d => d.time + ' – ' + d.price).join(' | ')}
          </span>`
        : ''
      }
  </div>

  <div class="card-body short">
  <div class="short-text">${item.short || ''}</div>
  <button class="view-btn">View details</button>
</div>

  <div class="card-body full">
    ${item.full}
  </div>
`;


    const header = card.querySelector('.card-header');
    const shortBody = card.querySelector('.short');
    const fullBody = card.querySelector('.full');
    const viewBtn = card.querySelector('.view-btn');

    // TITLE → short 
    header.onclick = () => {

      // Əgər FULL açıqdırsa → hamısını bağla
      if (fullBody.classList.contains('open')) {
        fullBody.classList.remove('open');
        shortBody.classList.remove('open');
        return;
      }

      // Əks halda → short toggle
      shortBody.classList.toggle('open');
    };

    // VIEW DETAILS → short bağla, full aç
    viewBtn.onclick = (e) => {
      e.stopPropagation();
      shortBody.classList.remove('open');
      fullBody.classList.toggle('open');
    };

    wrap.appendChild(card);
    setTimeout(() => card.classList.add('show'), i * 80);
  });
}

